package lang

import "net/url"

////////////////////////////////////////////////////////////////////////////////

type URI string

type URL string

////////////////////////////////////////////////////////////////////////////////

func (u URI) String() string {
	return string(u)
}

////////////////////////////////////////////////////////////////////////////////

func (u URL) String() string {
	return string(u)
}

func (u URL) Parse() (*url.URL, error) {
	str := u.String()
	return url.Parse(str)
}

func CreateURL(location *url.URL) URL {
	if location == nil {
		return ""
	}
	str := location.String()
	return URL(str)
}

